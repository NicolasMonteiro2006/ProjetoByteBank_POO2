    public void carregarUsuariosContaSalario() {
    try {
        contaSalarioDAO dao = new contaSalarioDAO();
        List<ContaSalario> lista = dao.listarUsuariosContaSalario();

        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setRowCount(0); 

        for (ContaSalario cs : lista) {
            modelo.addRow(new Object[]{
                cs.getId(),
                cs.getNome(),
                cs.getEmpresaContratante()
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar usuários: " + e.getMessage());
    }
}
    
