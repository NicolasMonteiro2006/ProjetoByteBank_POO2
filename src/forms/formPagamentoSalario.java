    private void btnPagarSalarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarSalarioActionPerformed
        try {
        int linha = tblUsuarios.getSelectedRow();
        if (linha == -1) {
            throw new Exception("Selecione um usuário da tabela.");
        }

        int idUsuario = (int) tblUsuarios.getValueAt(linha, 0);
        String empresa = (String) tblUsuarios.getValueAt(linha, 2); 
        double valor = Double.parseDouble(txtValorPagamento.getText().trim());
        
        

        if (valor <= 0) {
            throw new Exception("Informe um valor válido maior que zero.");
        }

        contaSalarioDAO dao = new contaSalarioDAO();
        dao.realizarPagamentoSalario(empresa, idUsuario, valor);
        contaCorrenteDAO.sacar(idUsuarioLogado, valor);
        carregarSaldo();

        JOptionPane.showMessageDialog(this, "Pagamento realizado com sucesso!");
        txtValorPagamento.setText("");
        
        if (telaTransferencia != null) {
        telaTransferencia.carregarSaldo(idUsuarioLogado);
        }
        if (telaContaCorrente != null) {
        telaContaCorrente.atualizarDadosContaCorrente();
        }


    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Digite um valor numérico válido.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnPagarSalarioActionPerformed

