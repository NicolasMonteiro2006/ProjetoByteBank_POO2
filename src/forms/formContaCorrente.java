    public void carregarDadosUsuario(int idUsuario) {
    try {
        contaCorrenteDAO dao = new contaCorrenteDAO();
        Conta conta = dao.obterInformacoesContaCorrente(idUsuario);

        txtNomeUsuario.setText(conta.getNome());
        txtSaldoDisponivel.setText(String.format("R$ %.2f", conta.getSaldo()));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar dados do usu√°rio: " + e.getMessage());
    }
}
      public void atualizarDadosContaCorrente() {
    carregarDadosUsuario(this.idUsuarioLogado);
    }

    
   

   


