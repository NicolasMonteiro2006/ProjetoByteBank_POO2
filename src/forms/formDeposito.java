    private void btnDepositoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositoActionPerformed
        try {
        String senha = new String(txtSenhaDepo.getPassword());
        String senhaConfirma = new String(txtConfSenhaDepo.getPassword());
        double valor = Double.parseDouble(txtValorDeposito.getText());

        if (senha.isEmpty() || senhaConfirma.isEmpty() || txtValorDeposito.getText().isEmpty()) {
            throw new Exception("Preencha todos os campos.");
        }

        if (!senha.equals(senhaConfirma)) {
            throw new Exception("As senhas não coincidem.");
        }

        
        if (!contaDAO.verificarSenha(idUsuarioLogado, senha)) {
            throw new Exception("Senha incorreta.");
        }

        
        contaCorrenteDAO.depositar(idUsuarioLogado, valor);

        
        historicoDAO.registrarTransacao(idUsuarioLogado, idUsuarioLogado, "Depósito", valor);

        
        carregarSaldo(idUsuarioLogado);
        carregarDepositosNaTabela();
        JOptionPane.showMessageDialog(this, "Depósito realizado com sucesso.");
        
       if (telaContaCorrente != null) {
        telaContaCorrente.atualizarDadosContaCorrente();           
    }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDepositoActionPerformed

