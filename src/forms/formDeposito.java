    public void carregarDepositosNaTabela() {
    try {
        HistoricoDAO dao = new HistoricoDAO();
        List<HistoricoTransacao> lista = dao.listarTransacoesPorTipo("Depósito");

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0); 

        for (HistoricoTransacao t : lista) {
        modelo.addRow(new Object[]{
        t.getNomePagante(),
        t.getTipo(),
        String.format("R$ %.2f", t.getValor()),
        t.getDataTransacao().toLocalDateTime()
    });
}

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar depósitos: " + e.getMessage());
    }
}



    

