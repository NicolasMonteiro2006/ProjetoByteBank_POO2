    private void carregarSaldo(int idUsuario) {
    try {
        double saldo = contaCorrenteDAO.buscarSaldoPorIdUsuario(idUsuario);
        txtSaldoDeposito.setText(String.format("R$ %.2f", saldo));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar saldo: " + e.getMessage());
     }
    }
    
